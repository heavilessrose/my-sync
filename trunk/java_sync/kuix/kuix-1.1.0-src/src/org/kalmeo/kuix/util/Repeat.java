package org.kalmeo.kuix.util;

/**
 * @author bbeaulant
 */
public class Repeat {

	public int repeatX;
	public int repeatY;

	/**
	 * Construct a {@link Repeat}
	 */
	public Repeat() {
	}

	/**
	 * Construct a {@link Repeat}
	 * 
	 * @param repeatX
	 * @param repeatY
	 */
	public Repeat(int repeatX, int repeatY) {
		this.repeatX = repeatX;
		this.repeatY = repeatY;
	}

}
