package org.kalmeo.kuix.util;

/**
 * @author bbeaulant
 */
public class Weight {

	public int weightx;
	public int weighty;

	/**
	 * Construct a {@link Weight}
	 */
	public Weight() {
	}

	/**
	 * Construct a {@link Weight}
	 * 
	 * @param weightx
	 * @param weighty
	 */
	public Weight(int weightx, int weighty) {
		this.weightx = weightx;
		this.weighty = weighty;
	}

}
